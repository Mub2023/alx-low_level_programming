
#include <stdio.h>

/**
 * add - add two integer
 * @a: input number
 * @b: input integer
 * Return: the result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integer
 * @a: input integer
 * @b: input integer
 * Return: the result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multbliy two integer
 * @a: input integer
 * @b: input integer
 * Return: the result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division two integer
 * @a: input integer
 * @b: input integer
 * Return: the result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - remainder of the division of two integer
 * @a: input integer
 * @b: input integer
 * Return: the result
 */
int mod(int a, int b)
{
	return (a % b);
}
